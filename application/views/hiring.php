  <li>
    The TAPS hosts a company's visit to the campus by processing the visit in a step-by-step manner
    <ol id="list1">
      <li>Invitations are sent to the organizations with relevant information and Job Announcement Form.(JAF)</li>
      <li>A mutually convenient date for the organization's visit could be finalized through consultation with the placement office.</li>
      <li>Companies fill the <a href="<?php echo base_url('assets/documents/JAF_NITW_2016-17_.docx')?>">Job Announcement Form</a> and can either send it by email/fax/courier it to the placement officer. The Job Announcement Form provides the primary basis of communicating the details of the positions offered to the candidates. It is therefore, highly desirable that the form is completed in all respects, and it would be advantageous if it were accompanied by relevant company literature with more details about the company.</li>
      <li>The Job Announcement is publicized and circulated amongst the students. Company posters (if sent) are displayed at prominent places on the campus.</li>
      <li>Eligible and interested students submit their applications either in the <a href="#">Student Bio-data Form</a> or the company format (if specified).</li>
      <li>The company visits the campus on the scheduled dates and gives a pre-placement presentation giving a brief corporate profile to the students following, which the selection procedure commences.</li>
    <li>Arrangements are made for conducting the written screening test or the company selection process.  </li>
    <li>Any specific requirement of company may be mentioned apriori so as to avoid any inconvenience during the selection procedure.</li>
    </ol>
  </li>
  <li>At the end of the selection process, the final list of selected candidates is to be given to the Placement Officer duly signed by the company official.</li>
  <li>Detailed offer letters must essentially be sent to In-charge TAPS, who in turn will serve them to the selected candidates and coordinate the return of acceptance letters duly signed by the candidates</li>
</ol>
